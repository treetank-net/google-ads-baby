#!/usr/bin/env node
// Regenerates the "Available Tools" section of README.md from server/src/tools/*.ts
// so the list can never drift from the code again. Run after adding/renaming tools:
//   node scripts/generate-readme-tools.mjs
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const toolsDir = join(root, 'server', 'src', 'tools');
const readmePath = join(root, 'README.md');

const SECTIONS = [
  ['auth.ts', 'Setup & maintenance'],
  ['read-accounts.ts', 'Read'],
  ['read.ts', 'Read'],
  ['read-analysis.ts', 'Read-only analysis (review loops — findings + suggested follow-up task + possible `prepare_*` actions; never mutate)'],
  ['read-history.ts', 'Mutation history'],
  ['write-prepare-campaigns.ts', 'Write preparation — campaigns, budgets, targeting'],
  ['write-prepare-ads.ts', 'Write preparation — ads & keywords'],
  ['write-prepare-assets.ts', 'Write preparation — assets & extensions'],
  ['write-confirm.ts', 'Confirmation & safety'],
];

const TOOL_RE = /server\.tool\(\s*'([^']+)'\s*,\s*((?:'(?:[^'\\]|\\.)*'\s*(?:\+\s*)?)+),/g;
const STRING_RE = /'((?:[^'\\]|\\.)*)'/g;

function firstSentence(description) {
  const clean = description
    .replace(/\\'/g, "'")
    .replace(/\s+/g, ' ')
    .replace(/ Returns a preview and (a )?confirmation token\.?/i, '')
    .trim();
  let cut = -1;
  for (let i = clean.indexOf('. '); i !== -1; i = clean.indexOf('. ', i + 1)) {
    const before = clean.slice(Math.max(0, i - 3), i);
    if (before === 'e.g' || before === 'i.e' || before.endsWith('etc')) continue;
    cut = i;
    break;
  }
  const sentence = cut === -1 ? clean : clean.slice(0, cut + 1);
  return sentence.replace(/\.$/, '');
}

const bySection = new Map();
for (const file of readdirSync(toolsDir).filter((f) => f.endsWith('.ts'))) {
  const source = readFileSync(join(toolsDir, file), 'utf8');
  const section = SECTIONS.find(([name]) => name === file)?.[1] || 'Other';
  for (const match of source.matchAll(TOOL_RE)) {
    const [, name, rawDescription] = match;
    const description = [...rawDescription.matchAll(STRING_RE)].map((m) => m[1]).join('');
    if (!bySection.has(section)) bySection.set(section, []);
    bySection.get(section).push(`- \`${name}\` — ${firstSentence(description)}`);
  }
}

const ordered = [...new Set(SECTIONS.map(([, title]) => title))].filter((t) => bySection.has(t));
if (bySection.has('Other')) ordered.push('Other');
const toolCount = [...bySection.values()].reduce((n, list) => n + list.length, 0);

const generated = [
  '<!-- TOOLS:BEGIN — generated by scripts/generate-readme-tools.mjs; do not edit by hand -->',
  '',
  ...ordered.flatMap((title) => [`${title}:`, '', ...bySection.get(title).sort(), '']),
  '<!-- TOOLS:END -->',
].join('\n');

const readme = readFileSync(readmePath, 'utf8');
const marked = readme.match(/<!-- TOOLS:BEGIN[\s\S]*?<!-- TOOLS:END -->/);
let next;
if (marked) {
  next = readme.replace(marked[0], generated);
} else {
  const section = readme.match(/## Available Tools\n[\s\S]*?(?=\n## )/);
  if (!section) throw new Error('README.md: neither TOOLS markers nor "## Available Tools" section found.');
  next = readme.replace(section[0], `## Available Tools\n\n${generated}\n`);
}
writeFileSync(readmePath, next, 'utf8');
console.log(`README.md: ${toolCount} tools across ${ordered.length} sections.`);
